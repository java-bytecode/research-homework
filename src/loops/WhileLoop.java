package loops;

public class WhileLoop {
	
	public static void main(String[] args) {
		// Entry Controlled Loop
		int i = 10;
		
		while(i < 20) {
			System.out.println(i);
			i++;
		}
		
		System.out.print("\n-----------------------\n\n");
		
		System.out.println("\nValue of i: " + i + "\n");
		
	}

}
